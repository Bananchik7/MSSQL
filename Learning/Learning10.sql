CREATE TABLE Ученики (
ID	INT PRIMARY KEY IDENTITY(1,1), Фамилия	NVARCHAR(50) NOT NULL, Предмет	NVARCHAR(50) NOT NULL,
Школа	NVARCHAR(50) NOT NULL,
Баллы	FLOAT CHECK ((Баллы >= 0) AND (Баллы <= 100)) NULL);

--Задание №1. В таблицу «Ученики» внести новую запись для ученика школы № 18 Трошкова, оценка которого по химии неизвестна.

INSERT INTO Ученики (Фамилия, Предмет, Школа)
VALUES (N'Трошкова', N'Химия', N'Школа №18');

--Задание №2. В таблицу «Ученики» внести три строки.

INSERT INTO Ученики (Фамилия, Предмет, Школа, Баллы)
